package com.storykeeper.config.database;

/**
 * Data Transfer Object for database configuration.
 * Contains all necessary information to establish a database connection.
 */
public class DatabaseConfig {
    
    private String host;
    private String port;
    private String databaseName;
    private String username;
    private String password;
    private String driverClassName;
    private String jdbcUrl;
    
    // Constructors
    public DatabaseConfig() {
    }
    
    public DatabaseConfig(String host, String port, String databaseName, 
                         String username, String password, String driverClassName) {
        this.host = host;
        this.port = port;
        this.databaseName = databaseName;
        this.username = username;
        this.password = password;
        this.driverClassName = driverClassName;
    }
    
    // Getters and Setters
    public String getHost() {
        return host;
    }
    
    public void setHost(String host) {
        this.host = host;
    }
    
    public String getPort() {
        return port;
    }
    
    public void setPort(String port) {
        this.port = port;
    }
    
    public String getDatabaseName() {
        return databaseName;
    }
    
    public void setDatabaseName(String databaseName) {
        this.databaseName = databaseName;
    }
    
    public String getUsername() {
        return username;
    }
    
    public void setUsername(String username) {
        this.username = username;
    }
    
    public String getPassword() {
        return password;
    }
    
    public void setPassword(String password) {
        this.password = password;
    }
    
    public String getDriverClassName() {
        return driverClassName;
    }
    
    public void setDriverClassName(String driverClassName) {
        this.driverClassName = driverClassName;
    }
    
    public String getJdbcUrl() {
        return jdbcUrl;
    }
    
    public void setJdbcUrl(String jdbcUrl) {
        this.jdbcUrl = jdbcUrl;
    }
}
